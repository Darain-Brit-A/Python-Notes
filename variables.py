# Variables are  container for a value have 4 types (string,integer, float, Boolean)

# Strings
# normal string assigned in to a variable
first_name = "Darain"
print(first_name)

# Printing using f string f means format
print(f"Hello {first_name}")

# Intergers
age = 19
print(f"My age is {age}.")

# Float means floating point number
quantity = 4.5
print(f"I am buying {quantity}kg of Ice Cream")

# Boolean mostly used in if
passed = True
print(f"Did i pass?\n{passed}")
