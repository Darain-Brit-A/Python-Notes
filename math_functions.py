x = 3.14
y = -5
z = 4

# rounds to the nearest whole integer
print(round(x))

# absolute value is the distance away from 0 or just makes negative positive
print(abs(y))

# power raises the base to the given power
print(pow(x,z))

# maximum returns the maximum value
print(max(x,y,z))

# minimum returns the minimum value of the numbers
print(min(x,y,z))


