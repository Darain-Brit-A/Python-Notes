# indexing = accessing elements of a sequence using []
#[start : end : step]

phone_number = "1234-5678-9012-3456"

print(phone_number[0])

# range
print(phone_number[1 : 5])

# skips every step 
print(phone_number[::2])

# negative goes in reverse order
print(phone_number[-1])

# reverse order
print(phone_number[::-1])