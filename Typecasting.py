#Typecasting it is the process of converting a variable from one data type to another 
name = "Darain"
age = 19
gpa = 3.99
is_student = True

#type function is used to get the type of the variable
print(type(name))
print(type(age))
print(type(gpa))
print(type(is_student))

#converting float to int
print(int(gpa))

#conveting integer to float
print(float(age))

#converting int to string
print(str(age))
print(type(str(age)))

#converting string to boolean
print(bool(name))